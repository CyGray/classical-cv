"""Hybrid vs LBPH-only vs SFace-only on the same held-out split.

Runs the three configurations through the *same* evaluation machinery
(``src/hybrid/evaluate.py``) so the comparison is apples-to-apples:

* **LBPH-only** = ``cv_only`` (classical fast path, no escalation - the CPU-only
  fallback),
* **SFace-only** = ``dl_only`` (every frame on the CNN), and
* **Hybrid**    = ``cascade`` (LBPH fast path + gated SFace escalation).

Writes ``reports/benchmark/hybrid_comparison.md`` (+ ``.json``). Every number is
measured by the run; the "done when" gate is hybrid fused accuracy >= LBPH-only
while escalation stays within the FPS budget.
"""

from __future__ import annotations

import argparse
import json
import time
from pathlib import Path

from src.classical_faces.detection import create_face_detector
from src.hybrid.evaluate import evaluate_hybrid
from src.hybrid.recognizer import (
    DEFAULT_LBPH_LABELS,
    DEFAULT_LBPH_MODEL,
    DEFAULT_SFACE_GALLERY,
    build_hybrid,
)

PROJECT_ROOT = Path(__file__).resolve().parents[2]

MODE_LABELS = [
    ("cv_only", "LBPH-only"),
    ("dl_only", "SFace-only"),
    ("cascade", "Hybrid (cascade)"),
]


def root_path(*parts: str) -> str:
    return str(PROJECT_ROOT.joinpath(*parts))


def resolve_path(value: str) -> str:
    p = Path(value)
    return str(p) if p.is_absolute() else str(PROJECT_ROOT / p)


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description="Compare hybrid vs LBPH-only vs SFace-only.")
    p.add_argument("--test-dir", default=root_path("data", "split_lasalle", "test"))
    p.add_argument("--impostor-dir", default=root_path("data", "lfw-dataset"))
    p.add_argument("--max-impostors", type=int, default=400)
    p.add_argument("--lbph-model", default=DEFAULT_LBPH_MODEL)
    p.add_argument("--lbph-labels", default=DEFAULT_LBPH_LABELS)
    p.add_argument("--sface-gallery", default=DEFAULT_SFACE_GALLERY)
    p.add_argument("--report-md", default=root_path("reports", "benchmark", "hybrid_comparison.md"))
    p.add_argument("--report-json", default=root_path("reports", "benchmark", "hybrid_comparison.json"))
    p.add_argument("--assume-cropped", action=argparse.BooleanOptionalAction, default=True)
    return p.parse_args()


def _fmt(value, suffix: str = "", nd: int = 2) -> str:
    if value is None:
        return "N/A"
    return f"{value:.{nd}f}{suffix}"


def main() -> int:
    args = parse_args()
    test_dir = Path(resolve_path(args.test_dir))
    impostor_dir = Path(resolve_path(args.impostor_dir)) if args.impostor_dir else None
    detector = create_face_detector("yunet")

    rows: list[dict] = []
    for mode, label in MODE_LABELS:
        print(f"\n[COMPARE] running {label} ({mode}) ...")
        hybrid = build_hybrid(
            mode=mode,
            lbph_model=resolve_path(args.lbph_model),
            lbph_labels=resolve_path(args.lbph_labels),
            sface_gallery=resolve_path(args.sface_gallery),
            require_sface=(mode != "cv_only"),
        )
        result = evaluate_hybrid(
            hybrid,
            detector=detector,
            test_dir=test_dir,
            known_names=set(hybrid.labels),
            assume_cropped=args.assume_cropped,
            impostor_dir=impostor_dir,
            max_impostors=args.max_impostors,
        )
        rows.append({"mode": mode, "label": label, **result})

    # --- markdown ---------------------------------------------------------- #
    lines: list[str] = []
    lines.append("# Hybrid vs LBPH-only vs SFace-only\n")
    lines.append(
        f"Genuine: `{test_dir}` (held-out clean split) | "
        f"Impostors: `{impostor_dir}` | thresholds: `src/hybrid/thresholds.json`.\n"
    )
    lines.append(
        f"_Generated by `src/benchmark/compare_hybrid.py` on "
        f"{time.strftime('%Y-%m-%d %H:%M:%S')}. All figures measured from this run._\n"
    )
    lines.append(
        "| Config | Rank-1 | Hit@gate | TAR | FRR | FAR | Escalation | End-to-end | ~FPS |"
    )
    lines.append("|---|---:|---:|---:|---:|---:|---:|---:|---:|")
    for row in rows:
        o, fm, esc, lat = row["overall"], row["fused_metrics"], row["escalation"], row["latency"]
        lines.append(
            f"| {row['label']} "
            f"| {_fmt(o['closed_set_rank1_percent'], '%')} "
            f"| {_fmt(o['hit_rate_percent'], '%')} "
            f"| {_fmt(fm['tar_percent'], '%')} "
            f"| {_fmt(fm['frr_percent'], '%')} "
            f"| {_fmt(fm['far_percent'], '%', 4)} "
            f"| {_fmt(esc['escalation_rate_percent'], '%')} "
            f"| {_fmt(lat['end_to_end_mean_ms'], ' ms')} "
            f"| {_fmt(lat['projected_fps'])} |"
        )

    cascade = next(r for r in rows if r["mode"] == "cascade")
    cv_only = next(r for r in rows if r["mode"] == "cv_only")
    hybrid_acc = cascade["overall"]["closed_set_rank1_percent"]
    lbph_acc = cv_only["overall"]["closed_set_rank1_percent"]
    gate_pass = hybrid_acc >= lbph_acc
    lines.append("\n## Gate (Phase 4 done-when)\n")
    lines.append(
        f"- Hybrid fused rank-1 (**{hybrid_acc:.2f}%**) "
        f"{'>=' if gate_pass else '<'} LBPH-only rank-1 (**{lbph_acc:.2f}%**): "
        f"**{'PASS' if gate_pass else 'FAIL'}**."
    )
    lines.append(
        f"- Hybrid escalation rate: **{cascade['escalation']['escalation_rate_percent']:.2f}%** "
        f"(engine usage {cascade['escalation']['engine_usage']}); "
        f"end-to-end **{cascade['latency']['end_to_end_mean_ms']:.2f} ms** "
        f"(~{cascade['latency']['projected_fps']} fps, off-device CPU)."
    )
    lines.append(
        "\n_FPS here is an off-device CPU projection (1000 / end-to-end ms). The "
        "Stage-3 >=30 fps / <100 ms budget is verified on the Pi 5 + NPU in Phase 6._"
    )

    md_path = Path(resolve_path(args.report_md))
    md_path.parent.mkdir(parents=True, exist_ok=True)
    md_path.write_text("\n".join(lines) + "\n", encoding="utf-8")

    json_path = Path(resolve_path(args.report_json))
    with json_path.open("w", encoding="utf-8") as f:
        json.dump(
            {"test_dir": str(test_dir), "impostor_dir": str(impostor_dir), "rows": rows},
            f,
            indent=2,
        )

    print("\n" + "\n".join(lines))
    print(f"\n[COMPARE] Markdown: {md_path}")
    print(f"[COMPARE] JSON: {json_path}")
    return 0 if gate_pass else 1


if __name__ == "__main__":
    raise SystemExit(main())
